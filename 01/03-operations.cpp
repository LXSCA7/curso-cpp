#include <iostream>

using namespace std;

int main() {
    int a = 3;
    int b = 7;

    // imprime os resultados
    cout << "soma = " << a + b << endl;
    cout << "diferenca = " << a - b << endl;
    cout << "multiplicacao = " << a * b << endl;
    cout << "divisao = " << (float)a / b << endl;

    return 0;
}